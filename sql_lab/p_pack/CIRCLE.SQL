CREATE OR REPLACE PACKAGE CIRC_AREA AS
PI NUMBER(3,2):=3.14;
FUNCTION AREA(RADIUS NUMBER) RETURN NUMBER;
FUNCTION CIRCUMFERENCE(RADIUS NUMBER) RETURN NUMBER;
END CIRC_AREA;
/

CREATE OR REPLACE PACKAGE BODY CIRC_AREA AS
RADIUS NUMBER(3,2):=3.14;
FUNCTION AREA(RADIUS NUMBER) RETURN NUMBER
    AS
    IS_NEGATIVE EXCEPTION;
    BEGIN
        IF RADIUS < 0 THEN RAISE IS_NEGATIVE;
        END IF;
        RETURN (CIRC_AREA.PI * RADIUS * RADIUS);
    EXCEPTION
        WHEN IS_NEGATIVE THEN RETURN NULL;
    END AREA;
FUNCTION CIRCUMFERENCE(RADIUS NUMBER) RETURN NUMBER
    AS
    IS_NEGATIVE EXCEPTION;
    BEGIN
        IF RADIUS < 0 THEN RAISE IS_NEGATIVE;
        END IF;
        RETURN (2 * RADIUS * CIRC_AREA.PI);
    EXCEPTION
        WHEN IS_NEGATIVE THEN RETURN NULL;
    END CIRCUMFERENCE;
END CIRC_AREA;