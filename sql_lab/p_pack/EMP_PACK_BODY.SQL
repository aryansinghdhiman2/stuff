CREATE OR REPLACE PACKAGE EMPLOYEE_20_PACK
AS
FUNCTION EMP_HIRE(EMP_ID EMPLOYEE_20.EMP_ID%TYPE,
                EMP_NAME EMPLOYEE_20.EMP_NAME%TYPE,
                JOB EMPLOYEE_20.JOB%TYPE,
                SALARY EMPLOYEE_20.SALARY%TYPE,
                MANAGERID EMPLOYEE_20.MANAGERID%TYPE,
                DEPT_NO EMPLOYEE_20.DEPT_NO%TYPE)
                RETURN EMPLOYEE_20.EMP_ID%TYPE;
FUNCTION EMP_INC(EMP_ID EMPLOYEE_20.EMP_ID%TYPE,
                PERC NUMBER DEFAULT 5)
                RETURN EMPLOYEE_20.SALARY%TYPE;
PROCEDURE EMP_FIRE(EMP_ID EMPLOYEE_20.EMP_ID%TYPE);
END EMPLOYEE_20_PACK;
/

CREATE OR REPLACE PACKAGE BODY EMPLOYEE_20_PACK
AS
FUNCTION EMP_HIRE(EMP_ID EMPLOYEE_20.EMP_ID%TYPE,
                EMP_NAME EMPLOYEE_20.EMP_NAME%TYPE,
                JOB EMPLOYEE_20.JOB%TYPE,
                SALARY EMPLOYEE_20.SALARY%TYPE,
                MANAGERID EMPLOYEE_20.MANAGERID%TYPE,
                DEPT_NO EMPLOYEE_20.DEPT_NO%TYPE)
                RETURN EMPLOYEE_20.EMP_ID%TYPE
    AS
    BEGIN
    INSERT INTO EMPLOYEE_20 VALUES (EMP_ID,EMP_NAME,JOB,SALARY,MANAGERID,DEPT_NO);
    DBMS_OUTPUT.PUT_LINE('EMPLOYEE CREATED');
    END EMP_HIRE;


FUNCTION EMP_INC(EMP_ID EMPLOYEE_20.EMP_ID%TYPE,
                PERC NUMBER DEFAULT 5)
                RETURN EMPLOYEE_20.SALARY%TYPE
    AS 
    TEMP_SAL EMPLOYEE_20.SALARY%TYPE;
    BEGIN
    SELECT EMPLOYEE_20.SALARY INTO TEMP_SAL FROM EMPLOYEE_20 WHERE EMPLOYEE_20.EMP_ID = EMP_ID;
    TEMP_SAL := TEMP_SAL+(TEMP_SAL * PERC/100);
    UPDATE EMPLOYEE_20 SET SALARY = TEMP_SAL WHERE EMPLOYEE_20.EMP_ID = EMP_ID;
    DBMS_OUTPUT.PUT_LINE('SALARY INCREMENTED');
    RETURN TEMP_SAL;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('EMPLOYEE NOT FOUND');
    END EMP_INC;


PROCEDURE EMP_FIRE(EMP_ID EMPLOYEE_20.EMP_ID%TYPE)
    AS
    BEGIN
    DELETE FROM EMPLOYEE_20 WHERE EMPLOYEE_20.EMP_ID = EMP_ID;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('EMPLOYEE NOT FOUND');
    END EMP_FIRE;
END EMPLOYEE_20_PACK;
