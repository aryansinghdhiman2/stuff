DECLARE
EMP_ID EMPLOYEE_20.EMP_ID%TYPE;
EMP_NAME EMPLOYEE_20.EMP_NAME%TYPE;
JOB EMPLOYEE_20.JOB%TYPE;
SALARY EMPLOYEE_20.SALARY%TYPE;
MANAGERID EMPLOYEE_20.MANAGERID%TYPE;
DEPT_NO EMPLOYEE_20.DEPT_NO%TYPE;
USE_DEF NUMBER;
INP NUMBER;
PERC NUMBER;
E_ID EMPLOYEE_20.EMP_ID%TYPE;
BEGIN
DBMS_OUTPUT.PUT_LINE('1.HIRE');
DBMS_OUTPUT.PUT_LINE('2.INCREASE SALARY');
DBMS_OUTPUT.PUT_LINE('3.FIRE');
DBMS_OUTPUT.PUT_LINE('ENTER NUMBER FOR CORESPONDING OPTION');
INP:=:INP;
CASE INP
    WHEN 1 THEN 
        DBMS_OUTPUT.PUT_LINE('ENTER DETAILS');
        EMP_ID:=:EMP_ID;
        EMP_NAME:=:EMP_NAME;
        JOB:=:JOB;
        SALARY:=:SALARY;
        MANAGERID:=:MANAGERID;
        DEPT_NO:=:DEPT_NO;
        E_ID:=EMPLOYEE_20_PACK.EMP_HIRE(EMP_ID,
                                  EMP_NAME,
                                  JOB,
                                  SALARY,
                                  MANAGERID,
                                  DEPT_NO);
        DBMS_OUTPUT.PUT_LINE('EMPLOYEE '|| E_ID || 'CREATED');
        WHEN 2 THEN
        EMP_ID:=:EMP_ID;
        DBMS_OUTPUT.PUT_LINE('USE DEFAULT VALUE?');
        USE_DEF:=:USE_DEF;
        CASE USE_DEF
            WHEN 0 THEN 
                SALARY:=EMPLOYEE_20_PACK.EMP_INC(EMP_ID);
            WHEN 1 THEN
                DBMS_OUTPUT.PUT_LINE('ENTER VALUE OF INCREMENT');
                PERC:=:PERC;
                SALARY:=EMPLOYEE_20_PACK.EMP_INC(EMP_ID,PERC);
            END CASE;
        DBMS_OUTPUT.PUT_LINE('INCREASE SALARY: '|| SALARY);
        WHEN 3 THEN
        DBMS_OUTPUT.PUT_LINE('ENTER ID');
        EMP_ID:=:EMP_ID;
        EMPLOYEE_20_PACK.EMP_FIRE(EMP_ID );
    END CASE;
END;

