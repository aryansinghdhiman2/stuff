DECLARE
CURSOR CUR IS SELECT * FROM EMPLOYEE_20 WHERE EMPLOYEE_20.EMP_ID = '&EMP_ID';
SAL EMPLOYEE_20.SALARY%TYPE;
TEMP CUR%ROWTYPE;
BEGIN
OPEN CUR;
FETCH CUR INTO TEMP;
SAL := TEMP.SALARY + 0.05 * TEMP.SALARY;
CLOSE CUR;
UPDATE EMPLOYEE_20 SET EMPLOYEE_20.SALARY = SAL WHERE EMPLOYEE_20.EMP_ID = TEMP.EMP_ID;
END;